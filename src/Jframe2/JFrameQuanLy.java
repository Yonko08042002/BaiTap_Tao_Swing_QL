    public void fillDataTable(){
        DefaultTableModel model =(DefaultTableModel) tbQuanLy.getModel();
        model.setRowCount(0);
        for (QuanLy ql : dao.getAllQuanLy()){
            Object dataRow[]=   new Object[10];
            dataRow[0]=ql.getMaTailieu();
            dataRow[1]=ql.getTenNhaXuatBan();
            dataRow[2]=ql.getSoBanPhathanh();
            dataRow[3]=ql.getTacGia();
            dataRow[4]=ql.getTenSach();
            dataRow[5]=ql.getSoTrang();
            dataRow[6]=ql.getSoPhatHanh();
            dataRow[7]=ql.getThangPhatHanh();
            dataRow[8]=ql.getNgayPhatHanh();
            dataRow[9]=ql.getTheLoai();
            model.addRow(dataRow);
        }
    }
