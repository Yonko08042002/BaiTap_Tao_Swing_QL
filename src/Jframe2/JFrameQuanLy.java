    public boolean validataFrom(){
        if(txtMaTaiLieu.getText().isEmpty() || txtTenNhaXuatBan.getText().isEmpty()){
            return false;
        }
        return true;
    }
    public QuanLy getModel(){
        QuanLy ql= new QuanLy();
        ql.setMaTailieu(Integer.parseInt(txtMaTaiLieu.getText()));
        ql.setTenNhaXuatBan(txtTenNhaXuatBan.getText());
        ql.setSoBanPhathanh(Integer.parseInt(txtSobanPhatHanh.getText()));
        ql.setTacGia(txtTacGia.getText());
        ql.setTenSach(txtTenSach.getText());
        ql.setSoTrang(Integer.parseInt(txtSoTrang.getText()));
        ql.setSoPhatHanh(Integer.parseInt(txtSoPhatHanh.getText()));
        ql.setThangPhatHanh(Integer.parseInt(txtNgay.getText()));
        ql.setThangPhatHanh(Integer.parseInt(txtThang.getText()));
        ql.setTheLoai(cboTheLoai.getSelectedItem().toString());
        return ql;
        
    }
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        //Thêm
        if(validataFrom()){
            QuanLy ql = getModel();
            try {
                if(dao.add(ql)>0){
                    JOptionPane.showMessageDialog(this, "Lưu thành công");
                    fillDataTable();
                
            }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,"Lỗi: "+e.toString());
            }
        }else{
            JOptionPane.showMessageDialog(this, "Bạn phải nhập đày đủ thông tin");
        }
    }//GEN-LAST:event_btnAddActionPerformed

