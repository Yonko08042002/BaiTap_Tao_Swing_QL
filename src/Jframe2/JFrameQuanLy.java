    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if(txtMaTaiLieu.getText().isEmpty() || Ma_TL.length()==0){
            JOptionPane.showMessageDialog(this, "bạn chauw nhập Mã để xóa");           
        }else{
            try {
                if (Ma_TL.length()>0) {
                    dao.del(Integer.parseInt(Ma_TL));
                }else{
                    dao.del(Integer.parseInt(txtMaTaiLieu.getText()));
                }
                fillDataTable();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Lỗi:"+e.toString());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

